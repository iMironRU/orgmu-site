// Телефоны подразделений: разбор и приведение к одному виду.
//
// На orgma.ru их записывали кто как — в справочнике на 121 подразделение
// набралось 17 разных написаний одного и того же номера:
//
//   (3532) 50-06-06 (доб. 662)      8 (3532) 50-06-06 доб.630
//   (3532)50-06-06 доб. 888         (3532) 50-06-06 (доб. 662, 650, 653)
//   8 (3532) 50-06-06 доб.613;      34-92-96
//
// Хуже того, у деканата лечебного факультета три номера слиплись без единого
// разделителя: «1-2 курс: (3532) 50-06-06 (доб. 641)3-4 курс: …». Такая
// строка не помещалась в колонку и рвала вёрстку справочника.
//
// Поэтому строку не «чистят» заменами, а разбирают: находят в ней все номера,
// а текст между ними считают подписью («1-2 курс»). На выходе — список, и
// вёрстка сама решает, как его показать.
//
// Код города подставляется, когда его в записи нет: весь справочник —
// Оренбург, это городская АТС университета. Если в строке несколько номеров и
// код указан только у первого («(3532) 50-06-06 (доб. 319), 50-06-08»),
// второй наследует его.

export type Phone = {
  /** Подпись перед номером, если она была: «1-2 курс». */
  label?: string;
  /** Как показывать: +7 (3532) 50-06-06, доб. 641 */
  display: string;
  /** Что подставить в tel: */
  tel: string;
};

const CITY = "3532"; // Оренбург

// Номер: необязательные 8/+7 и код города, затем городские XX-XX-XX, затем
// необязательный добавочный — «доб. 641», «(доб. 662, 650, 653)», «доб.630».
const RE =
  /(?:(?:\+?[78][\s-]*)?\(?\s*(\d{3,4})\s*\)?[\s-]*)?(\d{2})[-\s](\d{2})[-\s](\d{2})(?:\s*\(?\s*доб\.?\s*(\d+(?:\s*,\s*\d+)*)\s*\)?)?/g;

// Мусор, который остаётся между номерами и подписью не является.
const TRIM = /^[\s,;:.·—–-]+|[\s,;:.·—–-]+$/g;

export function parsePhones(raw?: string | null): Phone[] {
  if (!raw) return [];
  const s = raw.replace(/ /g, " ").trim();
  const out: Phone[] = [];
  let city = CITY;
  let prevEnd = 0;

  RE.lastIndex = 0;
  for (let m = RE.exec(s); m; m = RE.exec(s)) {
    const [full, code, a, b, c, ext] = m;
    // Код города запоминаем: следующий номер в той же строке, записанный без
    // кода, почти наверняка относится к той же АТС.
    if (code) city = code;

    const between = s.slice(prevEnd, m.index).replace(TRIM, "");
    prevEnd = m.index + full.length;

    const exts = ext ? ext.split(",").map((e) => e.trim()).filter(Boolean) : [];
    const local = `${a}-${b}-${c}`;
    const display = `+7 (${city}) ${local}` + (exts.length ? `, доб. ${exts.join(", ")}` : "");
    // Запятая в tel: — пауза перед добавочным; так набирают и Android, и iOS.
    const tel = `+7${city}${a}${b}${c}` + (exts.length ? `,${exts[0]}` : "");

    out.push({ ...(between ? { label: between } : {}), display, tel });
  }

  return out;
}

/** Одной строкой — там, где список не нужен (карточка подразделения). */
export function formatPhone(raw?: string | null): string {
  const list = parsePhones(raw);
  if (list.length === 0) return raw?.trim() ?? "";
  return list.map((p) => (p.label ? `${p.label}: ${p.display}` : p.display)).join("; ");
}
